    public boolean checktg()
    {
        boolean check=true;
        int batdau=Integer.parseInt ((String) txtBD.getSelectedItem());
        int ketthuc=Integer.parseInt ((String) txtKT.getSelectedItem());
        if(batdau>=ketthuc)
        {
            check=false;
        }
        return check;
    }
    public int Dem()
    {int dem=0;
        try {
            
            ketnoicsdl kn= new ketnoicsdl();
            ResultSet rs=kn.GetData("tbcalam");
            while(rs.next())
            {
                dem++;
            }
        } catch (SQLException ex) {
            Logger.getLogger(FormNhanVien.class.getName()).log(Level.SEVERE, null, ex);
        }
        return dem;
    }
    public void ShowTable()
    {
        DefaultTableModel model =(DefaultTableModel)tbCaLam.getModel();
        model.setRowCount(Dem());
        try {
            
            ketnoicsdl kn= new ketnoicsdl();
            ResultSet rs=kn.GetData("tbcalam");
            int i=0;
            while(rs.next())
            {
                tbCaLam.setValueAt(rs.getString("macalam"), i, 0);
                 tbCaLam.setValueAt(rs.getString("tencalam"), i, 1);
                  tbCaLam.setValueAt(rs.getString("batdau"), i, 2);
                   tbCaLam.setValueAt(rs.getString("ketthuc"), i, 3);
                    
                     i++;
            }
        } catch (SQLException ex) {
            Logger.getLogger(FormNhanVien.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
