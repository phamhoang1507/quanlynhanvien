    public boolean kttrung()
    {
        boolean check=true;
        try {
            ketnoicsdl  kn= new ketnoicsdl();
            ResultSet rs=kn.GetData("tbcalam");
            while(rs.next())
            {
                if(rs.getString("macalam").equalsIgnoreCase(txtMa.getText()))
                {
                    check=false;
                    break;
                }
            }
        } catch (SQLException ex) {
            Logger.getLogger(FomCaLam.class.getName()).log(Level.SEVERE, null, ex);
        }
        return check;
    }
    private void btnThemMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnThemMouseClicked
        // TODO add your handling code here:
        ketnoicsdl kn= new ketnoicsdl();
        String maca=txtMa.getText();
        String tennv=txtTen.getText();
        int batdau=Integer.parseInt ((String) txtBD.getSelectedItem());
        int ketthuc=Integer.parseInt ((String) txtKT.getSelectedItem());
        if(kttrung())
        {
            int kq=JOptionPane.showConfirmDialog(null, "Bạn Đã Chắc Chắn Thêm", "Thông Báo",JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
            if(kq==JOptionPane.YES_OPTION)
            {
                 if(ktrong(maca)==false||ktrong(tennv)==false||ktrong((String) txtBD.getSelectedItem())==false
                        ||ktrong((String) txtBD.getSelectedItem())==false)
                {
                      
                      JOptionPane.showMessageDialog(null, "Cần Nhập Đầy Đủ Thông Tin");
                     
                }
                else
                {
                     if(checktg()==true)
                      {
                          kn.ThemCaLam(maca, tennv, batdau, ketthuc);
                            JOptionPane.showMessageDialog(null, "Thêm Thành Công");
                      }
                     else
                     {
                          JOptionPane.showMessageDialog(null, "Bắt Đầu Phải Bé Hơn Kết Thúc");
                     }
                
                }
            }
             
        }
        else
        {
            JOptionPane.showMessageDialog(null, "Ca Làm Này Đã Có");
        }
       
        ShowTable();
    }//GEN-LAST:event_btnThemMouseClicked

