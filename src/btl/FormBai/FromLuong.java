     public boolean checknv()
     {
        boolean check=true;
          try {
            ketnoicsdl kn = new ketnoicsdl();
            //ResultSet rs=kn.GetData("tbcongviec");
            ResultSet rs=kn.MaNVLam();
            while(rs.next())
            {
                for(int i=0;i<tbLuong.getRowCount();i++)
                {
                    String ma=(String) tbLuong.getValueAt(i, 0);
                    //if(rs.getString("manv").equals(ma))
                     if(rs.getString("manv").equals(ma))
                    {
                        check=false;
                        
                    }
                    else
                    {
                        check=true;
                        break;
                        
                    }
                }
            }
        } catch (SQLException ex) {
            Logger.getLogger(FromLuong.class.getName()).log(Level.SEVERE, null, ex);
        }
          return check;
     }
