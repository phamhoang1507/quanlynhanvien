    public void ShowTable()
    {
        DefaultTableModel model=(DefaultTableModel) tbLuong.getModel();
        model.setRowCount(Dem());
         try {
              gio();
        luong();
            ketnoicsdl kn=new ketnoicsdl();
            ResultSet rs=kn.GetData("tbluong");
            int i=0;
            ResultSet rs1=kn.GetData("tbnhanvien");
            ResultSet rs2=kn.GetData("tbchucvu");
            int i1=0;
            int i2=0;
            while(rs.next())
            {
                tbLuong.setValueAt(rs.getString("manv"), i, 0);
                 tbLuong.setValueAt(rs.getString("loainv"), i, 2);
                  tbLuong.setValueAt(rs.getString("sogiolam"), i, 4);
                   tbLuong.setValueAt(rs.getString("luong"), i, 5);      
                   while(rs1.next())
                   {
                       if(rs.getString("manv").equalsIgnoreCase(rs1.getString("manv")))
                       {
                            tbLuong.setValueAt(rs1.getString("tennv"), i1, 1);
                             i1++;
                       }              
                   }
                   rs1=kn.GetData("tbnhanvien");
                   while(rs2.next())
                   {
                       if(rs.getString("loainv").equalsIgnoreCase(rs2.getString("loainv")))
                       {
                            tbLuong.setValueAt(rs2.getString("dongia"), i2, 3);
                            i2++;
                       }                       
                   }   
                   rs2=kn.GetData("tbchucvu");
                i++;
            }
        } catch (SQLException ex) {
            Logger.getLogger(FromLuong.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
