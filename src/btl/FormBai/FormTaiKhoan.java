    public int Dem()
    {
           int dem=0;
        try {
         
            ketnoidltaikhoan kn=new ketnoidltaikhoan();
            ResultSet rs=kn.GetTK();
            while(rs.next())
            {
                dem++;
            }
        } catch (SQLException ex) {
            Logger.getLogger(FormTaiKhoan.class.getName()).log(Level.SEVERE, null, ex);
        }
        return dem;
    }
    public void ShowTable()
    {
        DefaultTableModel model=(DefaultTableModel) tbTaiKhoan.getModel();
        model.setRowCount(Dem());
        try {
         
            ketnoidltaikhoan kn=new ketnoidltaikhoan();
            ResultSet rs=kn.GetTK();
            int i=0;
            while(rs.next())
            {
                String quyen="User";
                if(rs.getString("quyen").equalsIgnoreCase("1"))
                {
                    quyen="Admin";
                }
                tbTaiKhoan.setValueAt(rs.getString("taikhoan"), i, 0);
                tbTaiKhoan.setValueAt(rs.getString("matkhau"), i, 1);
                tbTaiKhoan.setValueAt(quyen, i, 2);
                i++;
            }
        } catch (SQLException ex) {
            Logger.getLogger(FormTaiKhoan.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
