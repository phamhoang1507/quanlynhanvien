    private void btnThemMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnThemMouseClicked
        // TODO add your handling code here:
        ketnoidltaikhoan kn =new ketnoidltaikhoan();
        String tk=txtTK.getText();
        String mk=txtMK.getText();
        int quyen;
        String a=(String) txtQuyen.getSelectedItem();
        if(a.equalsIgnoreCase("ADMIN"))
        {
            quyen=1;
        }
        else
        {
            quyen=2;
        }
        if(ktratrung())
        {
            int kq=JOptionPane.showConfirmDialog(null, "Bạn Đã Chắc Chắn Thêm", "Thông Báo",JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
            if(kq==JOptionPane.YES_OPTION)
            {
                   if(ktrong(tk)==false||ktrong(mk)==false||ktrong(a)==false)
                {
                      
                      JOptionPane.showMessageDialog(null, "Cần Nhập Đầy Đủ Thông Tin");
                }
                else
                {
                kn.ThemTKAD(tk, mk, quyen);
                JOptionPane.showMessageDialog(null, "Thêm Thành Công");
                }
            }
             
        }
        else
        {
            JOptionPane.showMessageDialog(null, "Tài Khoản Này Đã Có");
        }
       
        ShowTable();
    }//GEN-LAST:event_btnThemMouseClicked

